'''
Для последовательности из n > 1 целых чисел напечатать «ДА»,
если числа расположены в порядке не убывания или не возрастания (монотонная).
В первой строке задаётся количество чисел в
последовательности, а во второй - сами числа.
Исходные данные (в порядке неубывания):
5
1 1 2 2 3
Результат:
ДА
Исходные данные (в порядке невозрастания):
5
4 3 3 1 1
Результат:
ДА
Исходные данные (не монотонная):
6
9 2 4 0 7 3
Результат:
НЕТ
беру введёный символ, проверяю с максимумо, и потом этот максимум перезаписвыаю на новое число
'''
n = int(input("Сколько будет чисел? "))
prev = None  # Предыдущее число
non_decreasing = True  # Флаг для проверки неубывания
non_increasing = True  # Флаг для проверки невозрастания

for i in range(n):
    num = int(input(f"Введите {i + 1} число: "))

    if prev is not None:  # Сравнивать только начиная со второго числа
        if num < prev:  # Если текущее число меньше предыдущего
            non_decreasing = False
        if num > prev:  # Если текущее число больше предыдущего
            non_increasing = False

    prev = num  # Обновляем предыдущее число

    # Если оба флага сброшены, последовательность не монотонная
    if not non_decreasing and not non_increasing:
        break

# Если хотя бы один из флагов остался истинным, последовательность монотонна
if non_decreasing or non_increasing:
    print("ДА")
else:
    print("НЕТ")


