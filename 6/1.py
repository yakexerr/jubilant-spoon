cleaned = sp.replace(" ", "").lower()

# Находим середину
n = len(cleaned) // 2

sp_h1 = cleaned[:n]
# Если длина строки нечетная, игнорируем средний символ
if len(cleaned) % 2 != 0:
    sp_h2 = cleaned[n+1:]
else:
    sp_h2 = cleaned[n:]

# Проверяем равенство первой половины и развернутой второй
