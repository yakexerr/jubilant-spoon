# С помощью срезов проверить, является ли предложение палиндро-
# мом. Строка состоит из букв и пробелов, которые не должны учи-
# тываться во время проверки. Вывести «Да» или «Нет».

sp = "А роза упала на лапу Азора"
sp_l = sp.lower().replace(" ", "")

if sp_l == sp_l[::-1]:
    print("DA")
else:
    print("NET")


