# можно открывать файл в бинарном виде ,тогда пишем rb
# x - модификатор, открывающий файл для записи, но выдающий исключение, если файла не существовало
file = open("d.txt", "r") #r - модификатор read - просто читаем и ничего большего
print(file.read()) # read() - читает файл от начала и до конца
file.close() # закрываем файл когда закончили работу с ним

