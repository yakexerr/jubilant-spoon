# w - запись в файл,  w+ - и чтение и запись
# а - модификатор, позволяющий читать файл во время записи; a+ - модификатор, который открывает файл на дозапись
with open('s.txt', 'a+') as file:
    file.write("123")
    file.seek(0) # переведёт в начало строки ,тк из-за write мы будем в конце строки
    print(file.read())